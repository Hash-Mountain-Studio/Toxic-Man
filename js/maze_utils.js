// returns the x-maze and z-maze coordinates of given object
function localizer(x, z) {
  rounded_x = Math.round(x / 2);
  rounded_z = Math.round(z / 2);
  in_matrix_x = 1 + rounded_x + 7;
  in_matrix_z = 1 + rounded_z + 7;
  return { x: in_matrix_x, z: in_matrix_z };
}

function print_mazeMat() {
  var lines = [];
  maze_matrix.forEach((row) => {
    lines.push(row.join(" "));
  });
  console.log(lines.join("\n"));
}
